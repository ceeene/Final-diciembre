    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
       try{
          Integer id=Integer.parseInt(JTid.getText());
          productoActual=proData.buscarproducto(id);
          if(productoActual!=null){
              
              
              JTnombre.setText(productoActual.getNombreProducto());
              JTdescripcion.setText(productoActual.getDescripcion());
              JTprecio.setText(productoActual.getPrecioActual()+"");
              JTstock.setText(productoActual.getStock()+"");
              
              activo.setSelected(productoActual.isActivo());
              
          }
          
      }catch (NumberFormatException ex){
          JOptionPane.showMessageDialog(this,"Debe ingresar un numero de id valido");
      }  
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void botonguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonguardarActionPerformed
      
        try{
       
        String nombre=JTnombre.getText();
        String descripcion=JTdescripcion.getText();
        Integer precio=Integer.parseInt(JTprecio.getText());
        Integer stock=Integer.parseInt(JTstock.getText());
       
        
        if(nombre.isEmpty()|| descripcion.isEmpty()){
            JOptionPane.showMessageDialog(this,"No puede haber campos vacios");
            return;
        }
        
       
        Boolean estado=activo.isSelected();
        if (productoActual==null){
            
           productoActual=new Producto ( nombre, descripcion, precio, stock, estado);
           proData.guardarPoducto(productoActual);
            
        }
        
        }catch(NumberFormatException nfe){
         JOptionPane.showMessageDialog(this,"Debe ingresar un numero de id valido");   
        }  
         limpiarCampos();
    }//GEN-LAST:event_botonguardarActionPerformed

